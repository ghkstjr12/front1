public class Break문2 {

	public static void main(String[] args) {
		int menu=1;
		System.out.println("메뉴 1.10대,2 20대 3.30대, 4.종료");
		switch(menu) {
		case 1:
			System.out.println("등록선택");
			break;
		case 2:
			System.out.println("등록합니다 !!");
			break;
		case 3:
			System.out.println("조회합니다 !!");
			break;
		case 4:
			System.out.println("종료합니다 !!");
			break;

	}

}
}
